            "def cfar_2d(rd, guard=(4, 4), train=(8, 8), pfa=1e-5):\n",
            "    \"\"\"\n",
            "    Robust 2D CA-CFAR with boundary compensation and integral images.\n",
            "    \"\"\"\n",
            "    power = np.abs(rd)**2\n",
            "    rows, cols = power.shape\n",
            "    \n",
            "    # Integral image of power\n",
            "    S = np.zeros((rows + 1, cols + 1))\n",
            "    S[1:, 1:] = np.cumsum(np.cumsum(power, axis=0), axis=1)\n",
            "    \n",
            "    # Integral image of counts (to handle edges)\n",
            "    C_map = np.ones_like(power)\n",
            "    Sc = np.zeros((rows + 1, cols + 1))\n",
            "    Sc[1:, 1:] = np.cumsum(np.cumsum(C_map, axis=0), axis=1)\n",
            "\n",
            "    def get_area_sum(mat, r1, c1, r2, c2):\n",
            "        r1, r2 = np.clip([r1, r2+1], 0, rows)\n",
            "        c1, c2 = np.clip([c1, c2+1], 0, cols)\n",
            "        return mat[r2, c2] - mat[r1, c2] - mat[r2, c1] + mat[r1, c1]\n",
            "\n",
            "    det = np.zeros_like(power, dtype=bool)\n",
            "    \n",
            "    gr, gc = guard\n",
            "    tr, tc = train\n",
            "    \n",
            "    for i in range(rows):\n",
            "        for j in range(cols):\n",
            "            # Training + Guard window\n",
            "            total_sum = get_area_sum(S, i-tr-gr, j-tc-gc, i+tr+gr, j+tc+gc)\n",
            "            total_cnt = get_area_sum(Sc, i-tr-gr, j-tc-gc, i+tr+gr, j+tc+gc)\n",
            "            \n",
            "            # Guard window\n",
            "            guard_sum = get_area_sum(S, i-gr, j-gc, i+gr, j+gc)\n",
            "            guard_cnt = get_area_sum(Sc, i-gr, j-gc, i+gr, j+gc)\n",
            "            \n",
            "            n_cells = total_cnt - guard_cnt\n",
            "            if n_cells > 0:\n",
            "                noise_floor = (total_sum - guard_sum) / n_cells\n",
            "                alpha = n_cells * (pfa**(-1/n_cells) - 1)\n",
            "                if power[i, j] > alpha * noise_floor:\n",
            "                    det[i, j] = True\n",
            "    return det"
