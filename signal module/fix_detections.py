            "def simulate(targets, SNR_dB=25):\n",
            "    ref = []\n",
            "    surv = []\n",
            "    freqs = np.fft.fftfreq(N_SAMPLES)\n",
            "\n",
            "    for p in range(N_PULSES):\n",
            "        sig = generate_ofdm_stream(1)\n",
            "        sig /= np.sqrt(np.mean(np.abs(sig)**2))\n",
            "        ref.append(sig)\n",
            "\n",
            "        echo = np.zeros_like(sig, dtype=complex)\n",
            "        for t in targets:\n",
            "            delay = (2 * t['R'] / C) * FS\n",
            "            fd = 2 * t['v'] / LAMBDA\n",
            "            \n",
            "            # Fractional delay and Doppler\n",
            "            sig_f = np.fft.fft(sig)\n",
            "            shifted = np.fft.ifft(sig_f * np.exp(-1j * 2 * np.pi * freqs * delay))\n",
            "            doppler_phase = np.exp(1j * 2 * np.pi * fd * p * PRI)\n",
            "            \n",
            "            # Radar Equation: Power ~ 1/R^4 -> Amplitude ~ 1/R^2\n",
            "            # Normalized at 100m for simulation stability\n",
