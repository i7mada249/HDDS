            "def process(ref, surv):\n",
            "    # Range Compression with Chebychev or Hanning for better sidelobe control\n",
            "    # We use Hanning here for simplicity but apply it to the correlation\n",
            "    corr = fft(surv, axis=1) * np.conj(fft(ref, axis=1))\n",
            "    corr *= np.hanning(N_SAMPLES)\n",
            "    rp = ifft(corr, axis=1)\n",
            "\n",
            "    # MTI: 3-pulse canceller for better notch at DC\n",
            "    # y[n] = x[n] - 2x[n-1] + x[n-2]\n",
            "    mti = rp[2:] - 2*rp[1:-1] + rp[:-2]\n",
            "    \n",
            "    # Doppler Processing with Windowing\n",
            "    mti *= np.hanning(mti.shape[0])[:, np.newaxis]\n",
            "    rd = fftshift(fft(mti, axis=0), axes=0)\n",
            "    return rd"
